class Solution:
    def rearrange(self,arr, n):
        arr1=[] # pos elements
        arr2=[] # neg elements
        for i in arr:
            if i<0:
                arr2.append(i)
            else:
                arr1.append(i)
        p,ne,i=0,0,0
        while i<n:
            if p!=len(arr1):
                arr[i]=arr1[p]
                i+=1
                p+=1
            if ne!=len(arr2):
                arr[i]=arr2[ne]
                i+=1
                ne+=1
        

