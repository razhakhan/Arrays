'''

Example 1:

Input:
N = 4, K = 6
arr[] = {1, 5, 7, 1}
Output: 2
Explanation: 
arr[0] + arr[1] = 1 + 5 = 6 
and arr[1] + arr[3] = 5 + 1 = 6.

Example 2:

Input:
N = 4, K = 2
arr[] = {1, 1, 1, 1}
Output: 6
Explanation: 
Each 1 will produce sum 2 with any 1.

'''



#User function Template for python3

class Solution:
    def getPairsCount(self, arr, n, k):
        from collections import defaultdict
        dic={}
        dic=defaultdict(lambda:0,dic)
        c=0
        for a in arr:
            b=k-a
            if b in dic:
                c+=dic[b]
            dic[a]+=1
        return c

