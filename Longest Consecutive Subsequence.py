class Solution:
    
    # arr[] : the input array
    # N : size of the array arr[]
    
    #Function to return length of longest subsequence of consecutive integers.
    def findLongestConseqSubseq(self,arr, N):
        maxind=max(arr)
        l=[0]*(maxind+1)
        for i in arr:
            l[i]=1
        maxc=0
        c=0
        for i in range(maxind+1):
            if l[i]==1:
                c+=1
            else:
                c=0
            maxc=max(maxc, c)
        return maxc


